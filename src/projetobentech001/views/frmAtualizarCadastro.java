     public void povoarJtable()
    {
        java.sql.Connection conn = null;
        
        ResultSet rs = null;
        
        java.sql.PreparedStatement stmt = null;
        
        conn = ConexaoMyDB.getConexao();//conectar ao banco
        
        String sql = "SELECT * FROM tb_cadastro WHERE id_tb_cadastro = '" + CadastroCliente.id_tb_cadastro + "'";
       
        try{
            
            stmt = conn.prepareStatement(sql);
            stmt.execute();
            
            rs = stmt.executeQuery();
            
            rs.next();
            
            txtCodigo.setText(rs.getString("codigo_cliente"));
            txtNome.setText(rs.getString("nome"));
            txtSobrenome.setText(rs.getString("sobrenome"));
            txtCpf.setText(rs.getString("cpf"));
            
            //tratamento de data
            DateFormat data = new SimpleDateFormat("dd/MM/yyyy");
       
            txtData.setText(data.format(rs.getDate("data"))); 
            txtIdade.setText(rs.getString("idade"));
            cbxSexo.setSelectedItem(rs.getString("sexo"));
                                   
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Erro ao mostrar no banco de dados. Erro: " + ex);
        }
        finally
        {
            ConexaoMyDB.fecharConexao(conn, stmt, rs);
        }
        
    }
    
    
    public void atualizarDados(){
        
        Connection conn = null;
        
        conn = ConexaoMyDB.getConexao();//conectando ao banco de dados
        
           
        String sql = "UPDATE tb_cadastro SET nome = ?, sobrenome = ?, cpf = ?,"
                + "data = ?, idade = ?, sexo = ?" 
                + "WHERE codigo_cliente = ?";
        
        conn = ConexaoMyDB.getConexao();//conectar ao banco de dados
        
        PreparedStatement stmt = null;
       
        
        try
        {
                        
            stmt = conn.prepareStatement(sql);
            stmt.setString(1, CadastroCliente.nome_cliente);
            stmt.setString(2, CadastroCliente.sobrenome_cliente);
            stmt.setString(3, CadastroCliente.cpf_cliente);
            stmt.setDate(4, CadastroCliente.data_cliente);
            stmt.setInt(5, CadastroCliente.idade_cliente);
            stmt.setString(6, CadastroCliente.sexo_cliente);
            stmt.setString(7, CadastroCliente.codigo_cliente);            
            
            stmt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso !!!!");
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar no banco de dados. Erro: " + ex);
        }
        finally
        {
            ConexaoMyDB.fecharConexao(conn, stmt);
        }
        
        this.dispose();
    }
     
          
    
